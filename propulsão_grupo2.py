def simular_turbohelice(dados, fora_de_projeto=False, N2=1.0):
    Prc = dados["Prc"]
    Prtl = dados["Prtl"]
    T04 = dados["T04"]

    eta_compressor = dados["eta_compressor"]
    eta_turb_comp = dados["eta_turb_comp"]
    eta_turb_livre = dados["eta_turb_livre"]

    if fora_de_projeto:
        corr_Prc = -6.0730*N2**3 + 14.821*N2**2 - 10.042*N2 + 2.2915
        corr_T04 = 0.81821*N2**2 - 0.22401*N2 + 0.41842
        corr_eta_c = -1.1234*N2**2 + 2.1097*N2 + 0.018617
        corr_turb_comp = -0.06749*N2**2 + 0.25640*N2 + 0.81153
        corr_m_ar = -6.6970*N2**3 + 17.001*N2**2 - 12.170*N2 + 2.8717
        corr_eta_turb_livre = 19.062*N2**3 - 52.456*N2**2 + 47.887*N2 - 13.489
        corr_Prtl = -18.063*N2**3 + 42.469*N2**2 - 31.480*N2 + 8.0681

        Prc *= corr_Prc
        Prtl *= corr_Prtl
        T04 *= corr_T04
        eta_compressor *= corr_eta_c
        eta_turb_comp *= corr_turb_comp
        eta_turb_livre *= corr_eta_turb_livre

        m_ar = (
            dados["m_ar_padrao"]
            * (dados["Pa"] / dados["Ta"])
            / (101.3 / 288.15)
            * corr_m_ar
        )
    else:
        m_ar = dados["m_ar_padrao"] * (dados["Pa"] / dados["Ta"]) / (101.3 / 288.15)

    # ENTRADA DE AR
    T02 = dados["Ta"] * (1 + ((dados["gama_inlet"] - 1) / 2) * dados["M"]**2)

    P02 = dados["Pa"] * (
        1 + dados["eta_inlet"] * (T02 / dados["Ta"] - 1)
    ) ** (dados["gama_inlet"] / (dados["gama_inlet"] - 1))

    # COMPRESSOR
    P03 = P02 * Prc

    T03 = T02 * (
        1
        + (Prc ** ((dados["gama_compressor"] - 1) / dados["gama_compressor"]) - 1)
        / eta_compressor
    )

    # CÂMARA DE COMBUSTÃO
    P04 = P03 * (1 - dados["perda_camara"])

    f = (
        dados["Cp_combust"] * (T04 - T03)
    ) / (
        dados["eta_combust"] * dados["PCI"]
        - dados["Cp_combust"] * T04
    )

    m_f = f * m_ar
    m_total = m_ar * (1 + f)

    # TURBINA DO COMPRESSOR
    # Modelo usado no Exemplo 7.1 do livro
    T05 = T04 - (T03 - T02)

    T05s = T04 - (T04 - T05) / eta_turb_comp

    P05 = P04 * (T05s / T04) ** (
        dados["gama_turb_comp"] / (dados["gama_turb_comp"] - 1)
    )

    # TURBINA LIVRE
    P06 = P05 / Prtl

    T06s = T05 * (P06 / P05) ** (
        (dados["gama_turb_livre"] - 1) / dados["gama_turb_livre"]
    )

    W_tl_s = dados["Cp_turb_livre"] * (T05 - T06s)
    W_tl = eta_turb_livre * W_tl_s

    # Para reproduzir a planilha do livro, ela usa vazão arredondada 8,7 kg/s
    m_total_potencia = dados.get("m_turbina_potencia", round(m_total, 1))

    POT_tl = W_tl * m_total_potencia
    T06 = T05 - W_tl / dados["Cp_turb_livre"]

    # BOCAL
    if P06 > dados["Pa"]:
        Cp_bocal = dados.get("Cp_bocal_hot", 1.15916) * 1000

        T7s = T06 * (dados["Pa"] / P06) ** (
            (dados["gama_bocal_hot"] - 1) / dados["gama_bocal_hot"]
        )

        u_s = np.sqrt(
            2 * dados["eta_bocal_hot"] * Cp_bocal * (T06 - T7s)
        )
    else:
        T7s = T06
        u_s = 0.0

    # HÉLICE / CAIXA
    POT_eixo = dados["eta_gearbox"] * POT_tl
    POT_util = dados["eta_prop"] * POT_eixo

    u = dados["M"] * np.sqrt(
        dados["gama_atm"] * dados["R_medio"] * dados["Ta"]
    )

    if abs(u) > 1e-9:
        empuxo_helice_kN = (POT_util * 1000 / u) / 1000
    else:
        empuxo_helice_kN = np.nan

    # EMPUXO DO BOCAL
    empuxo_especifico_bocal_ms = (1 + f) * u_s - u
    empuxo_especifico_bocal_kNs_kg = empuxo_especifico_bocal_ms / 1000
    empuxo_bocal_kN = m_ar * empuxo_especifico_bocal_ms / 1000

    if np.isfinite(empuxo_helice_kN):
        empuxo_total_kN = empuxo_bocal_kN + empuxo_helice_kN
    else:
        empuxo_total_kN = np.nan

    # CONSUMO
    BSFC = m_f / POT_tl if POT_tl > 0 else np.nan
    EBSFC = BSFC if abs(u) <= 1e-9 else np.nan
    TSFC = m_f / empuxo_total_kN if empuxo_total_kN > 0 else np.nan

    return {
        "PARÂMETROS OPERACIONAIS": {
            "Mach de voo": dados["M"],
            "Velocidade de voo [m/s]": u,
            "Rotação N2": N2,
        },

        "VAZÕES MÁSSICAS": {
            "Vazão de ar [kg/s]": m_ar,
            "Vazão de combustível [kg/s]": m_f,
            "Razão combustível/ar": f,
            "Vazão na turbina [kg/s]": m_total,
        },

        "ESTADOS TERMODINÂMICOS": {
            "Temperatura T02 [K]": T02,
            "Pressão P02 [kPa]": P02,
            "Temperatura T03 [K]": T03,
            "Pressão P03 [kPa]": P03,
            "Temperatura T04 [K]": T04,
            "Pressão P04 [kPa]": P04,
            "Temperatura T05 [K]": T05,
            "Pressão P05 [kPa]": P05,
            "Temperatura T06s [K]": T06s,
            "Temperatura T06 [K]": T06,
            "Pressão P06 [kPa]": P06,
        },

        "TURBINA LIVRE": {
            "Trabalho isentrópico [kJ/kg]": W_tl_s,
            "Trabalho real [kJ/kg]": W_tl,
            "Potência da turbina [kW]": POT_tl,
            "Potência da turbina [hp]": POT_tl * 1.34102,
        },

        "BOCAL": {
            "Velocidade de saída do bocal [m/s]": u_s,
            "Empuxo Específico do bocal [m/s]": empuxo_especifico_bocal_ms,
            "Empuxo Específico do bocal [kN.s/kg]": empuxo_especifico_bocal_kNs_kg,
            "Empuxo do bocal [kN]": empuxo_bocal_kN,
        },

        "HÉLICE E CAIXA": {
            "Potência na saída da caixa de redução [kW]": POT_eixo,
            "Potência útil na hélice [kW]": POT_util,
            "Empuxo da hélice [kN]": empuxo_helice_kN,
            "Empuxo total [kN]": empuxo_total_kN,
        },

        "CONSUMO": {
            "Consumo de combustível [kg/s]": m_f,
            "Consumo esp. no eixo BSFC [kg/(kW.s)]": BSFC,
            "Consumo esp. equivalente EBSFC [kg/(kW.s)]": EBSFC,
            "TSFC [kg/(kN.s)]": TSFC,
        },
    }

